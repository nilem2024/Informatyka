#include <iostream>
using namespace std;

int main() {
    int a = 5, b = 3;
    cout << "Liczba a: " << a << endl;
    cout << "Liczba b: " << b << endl;
    cout << "Suma: " << a + b << endl;
    cout << "Roznica: " << a - b << endl;
    cout << "Iloczyn: " << a * b << endl;
    return 0;
}
